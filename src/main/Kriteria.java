    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        int row = TblKriteria.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(null, "Pilih data yang akan diubah");
        } else {
            String namaKriteria = txtNama.getText();
            String tipe = CmbTipe.getSelectedItem().toString();
            int bobot = Integer.parseInt(TxtBobot.getText());

            try {
                con = Koneksi.connect();
                String value = TblKriteria.getValueAt(row, 0).toString();
                pst = con.prepareStatement("UPDATE kriteria SET tipe=?, bobot=? WHERE id=?");
                pst.setString(1, tipe);
                pst.setInt(2, bobot);
                pst.setString(3, value);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data berhasil diubah");
                tampilData();
                clearFields();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Edit Data Gagal: "+ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnEditActionPerformed

