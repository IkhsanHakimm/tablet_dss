    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        String namaKriteria = txtNama.getText();
        String tipe = CmbTipe.getSelectedItem().toString();
        int bobot = Integer.parseInt(TxtBobot.getText());
        
        try {
            con = Koneksi.connect();
            pst = con.prepareStatement("insert into kriteria (nama_kriteria, tipe, bobot) values (?, ?, ?)");
            pst.setString(1, namaKriteria);
            pst.setString(2, tipe);
            pst.setInt(3, bobot);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil ditambahkan");
            tampilData();
            clearFields();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Tambah Data Gagal: "+ex.getMessage());
        }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int row = TblKriteria.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(null, "Pilih data yang akan dihapus");
        } else {
            try {
                con = Koneksi.connect();
                String value = TblKriteria.getValueAt(row, 0).toString();
                pst = con.prepareStatement("DELETE FROM kriteria WHERE id = ?");
                pst.setString(1, value);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
                tampilData();
                clearFields();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Hapus Data Gagal: "+ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

